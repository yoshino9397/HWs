package parent;

public class animal {
	private int height;
	private int weight;
	protected String animalType;
	private String bloodType;

	public int getHeight() {
		return height;
	}

	public int getWeight() {
		return weight;
	}

	public String getAnimalType() {
		return animalType;
	}

	public String getBloodType() {
		return bloodType;
	}

}
